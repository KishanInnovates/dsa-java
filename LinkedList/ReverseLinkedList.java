public class ReverseLinkedList {
    public static void main(String[] args) {

    }

    public static Node reverse(Node head) {
        Node prev = null;
        Node present = head;
        Node next = present.next;

        while (present != null) {
            present.next = prev;
            prev = present;
            present = next;
            if (next != null) {
                next = next.next;
            }
        }
        return prev;
    }
}
